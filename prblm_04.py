# Fruit Ripeness Checker
Fruit = input("Enter Your Fruit Name:")
Color = input("Enter Your Fruit Color:")
if Fruit == "Banana":
    if Color == "Green" :
     print("the Banana is Unripe")
    
    elif Color == "Yellow":
     print("the Banana is Ripe")
    
    elif Color == "Brown":
     print("The Banana is Overripe")

else:
    print("No data found")
    
